package day01_variables;

public class C5_Variables4 {

	public static void main(String[] args) {
		
		int sayi1=10;
	    int sayi2=4;
	    double sayi3=4;
	    
	    
	    System.out.println(sayi1/sayi2); // 10/4=2 oldu çünkü 2'side int tamsayı old için tam sayı verir.
	    
	    System.out.println(sayi1/sayi3); // 10/4=2.5 sayi3 double old için sonucu virgüllü verir.
	    
	    
	    
	    

	}

}
