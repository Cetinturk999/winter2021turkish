package day18_whileLoop;

public class dnm6 {

	public static void main(String[] args) {
		
		// verilen sayinin asal olup olmadýgýný bulan 
		// bir program yazýn
		
		int sayi=7;
		int bolen=2;
		
		while(bolen<sayi) {
			if (sayi%bolen!=0) {
				System.out.println("Asal sayi degil");
			} else {
                System.out.println("Asal sayi");
			}
			bolen++;
		}

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
